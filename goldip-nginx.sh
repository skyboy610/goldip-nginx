این تنظیمات روی Xray-core واقعاً اثر دارن (نه فقط ظاهری)، چون مستقیماً الگوی بسته‌ها روی سیم رو تغییر می‌دن. اینا فیلدهایی هستن که توی مستندات رسمی Xray-core تأیید شدن:

**۱) Padding تصادفی (xPaddingBytes) — جلوگیری از شناسایی بر اساس سایز ثابت بسته**

این فیلد توی `xhttpSettings.extra` قرار می‌گیره، نه `xPaddingMethod` که قبلاً امتحان کردیم و خطا می‌داد (احتمالاً اون اسم فیلد قدیمی/اشتباه بوده یا با نسخه‌ی نصب‌شده‌ی شما هم‌خوان نبوده):

```json
"xhttpSettings": {
  "path": "/ws-goldip",
  "mode": "auto",
  "extra": {
    "xPaddingBytes": "100-1000",
    "xmux": {
      "maxConcurrency": 128,
      "hMaxRequestTimes": 1000,
      "hMaxReusableSecs": 3600
    },
    "scMaxEachPostBytes": 1000000,
    "scMaxBufferedPosts": 30,
    "noSSEHeader": false
  }
}
```

**توضیح هر فیلد:**
- `xPaddingBytes: "100-1000"` → به هر فریم/درخواست یک padding تصادفی بین ۱۰۰ تا ۱۰۰۰ بایت اضافه می‌کنه، که جلوی شناسایی پروتکل بر اساس "سایز دقیق پکت" رو می‌گیره
- `xmux` → کنترل multiplexing: `maxConcurrency` تعداد استریم هم‌زمان روی یک کانکشن HTTP، `hMaxReusableSecs` حداکثر طول عمر یک کانکشن قبل از باز شدن کانکشن جدید (به‌جای یک تونل ابدی)، `hMaxRequestTimes` حداکثر تعداد درخواست روی یک کانکشن قبل از rotate شدن
- `scMaxEachPostBytes` / `scMaxBufferedPosts` → اندازه و تعداد بافر POST ها در حالت stream-up، که الگوی آپلود رو منظم‌تر/واقعی‌تر می‌کنه

**۲) قبل از اعمال روی سرور پروداکشن، حتماً تست کن:****۳) چند نکته‌ی تکمیلی:**

- این تنظیمات رو فقط روی اینباندهای `xhttp` می‌تونی بذاری (مثل همون CDN-سازگارهایی که توی اسکریپت نصب نگینکس مشخص کردیم). برای `ws`/`httpupgrade` فیلد `xPaddingBytes` وجود نداره چون اون پروتکل‌ها ساختار HTTP استریم‌دار ندارن.
- قبل از ادیت، حتماً از فایل `x-ui.db` بک‌آپ بگیر (دقیقاً همون کاری که اسکریپت نصب خودمون هم قبل از تغییر دیتابیس انجام می‌ده).
- نسخه‌ی Xray-core نصب‌شده رو با `xray version` چک کن — این فیلدها (`xPaddingBytes`, `xmux`) توی نسخه‌های جدیدتر (حدود ۱.۸+) اضافه شدن؛ اگه نسخه‌ی قدیمی‌تری داری، اول باید آپدیت کنی.
- بعد از اعمال، با `xray run -test` مطمئن شو کانفیگ valid هست، بعد سرویس رو ری‌استارت کن — این از قطعی سرویس برای کاربرهای فعلی جلوگیری می‌کنه.

اگه بخوای، می‌تونم یک تابع کوچیک به اسکریپت GoldIP اضافه کنم که این تنظیمات padding/xmux رو خودکار روی همه‌ی اینباندهای xhttp موجود در دیتابیس اعمال کنه (شبیه همون منطق `auto_build_locations` که قبلاً نوشتیم) — اینطوری دیگه لازم نیست دستی توی پنل برای هر اینباند تکرارش کنی. می‌خوای این قابلیت رو هم به اسکریپت اضافه کنم؟
